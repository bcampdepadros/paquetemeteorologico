#' Suma dos números
#'
#' Esta función toma dos números y devuelve su suma.
#'
#' @param x El primer número (número).
#' @param y El segundo número (número).
#' @return La suma de los dos números (número).
#' @examples
#' suma(2, 3) # Devuelve 5
#' suma(-1, 5) # Devuelve 4
#' @export
suma <- function(x, y) {
  return(x + y)
}

